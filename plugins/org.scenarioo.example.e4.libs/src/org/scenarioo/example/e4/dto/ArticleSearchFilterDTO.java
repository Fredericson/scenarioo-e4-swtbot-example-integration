package org.scenarioo.example.e4.dto;

public class ArticleSearchFilterDTO {

	private static Integer DEFAULT_PAGE_COUNT = 30;
	private static Integer DEFAULT_PAGE_START = 0;

	private Integer count;
	private Integer start;
	private String articleNumber;
	private String description;
	private String unit;

	public ArticleSearchFilterDTO() {
		this.setCount(DEFAULT_PAGE_COUNT);
		this.setStart(DEFAULT_PAGE_START);
	}

	/**
	 * @return the count
	 */
	public Integer getCount() {
		return count;
	}

	/**
	 * @param count
	 *            the count to set
	 */
	public void setCount(final Integer count) {
		this.count = count;
	}

	/**
	 * @return the start
	 */
	public Integer getStart() {
		return start;
	}

	/**
	 * @param start
	 *            the start to set
	 */
	public void setStart(final Integer start) {
		this.start = start;
	}

	/**
	 * @return the articleNumber
	 */
	public String getArticleNumber() {
		return articleNumber;
	}

	/**
	 * @param articleNumber
	 *            the articleNumber to set
	 */
	public void setArticleNumber(final String articleNumber) {
		this.articleNumber = articleNumber;
	}

	/**
	 * @return the unit
	 */
	public String getUnit() {
		return unit;
	}

	/**
	 * @param unit
	 *            the unit to set
	 */
	public void setUnit(final String unit) {
		this.unit = unit;
	}

	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}

	/**
	 * @param description
	 *            the description to set
	 */
	public void setDescription(final String description) {
		this.description = description;
	}

}
